void ucb_dflash_burst_program(uint32 addr, uint32 *pmem)
{
	volatile uint32 i;
	volatile uint32 low32bit, high32bit;
	volatile uint32 pmuid;

	// Clear the Erase State Flag DMU_HF_OPERATION.ERASE
	//DMU_HF_CONTROL.B.CERASE = 1;

	// Clear Status
	MEM (0xAF005554) = 0xFA;
	pmuid = PMU_ID.U;

	// Enter Page Mode DFLASH
	MEM (0xAF005554) = 0x5D;
	pmuid = PMU_ID.U;

	// Load Page
	i = 0;
	while (i < 8) // 4 * 8 byte = 32 byte
	{
		low32bit = pmem[i];  //load page
		high32bit = pmem[i + 1];
		MEM (0xAF0055F0) = low32bit;
		pmuid = PMU_ID.U;
		MEM (0xAF0055F4) = high32bit;
		pmuid = PMU_ID.U;
		i += 2;
	}

	while(DMU_HF_STATUS.B.D0BUSY);

	// Write Burst
	MEM (0xAF00AA50) = (uint32) addr;
	pmuid = PMU_ID.U;
	MEM (0xAF00AA58) = (uint32) 0x00;
	pmuid = PMU_ID.U;
	MEM (0xAF00AAA8) = (uint32) 0xA0;
	pmuid = PMU_ID.U;
	MEM (0xAF00AAA8) = (uint32) 0xA6;
	pmuid = PMU_ID.U;
	while(!DMU_HF_OPERATION.B.PROG);
	while(DMU_HF_STATUS.B.D0BUSY);
}

