#define MEM *(volatile uint32*)

uint32 pflash_erase(uint32 addr, uint32 cnt)
{
	volatile uint32 pmuid;

	if (cnt >= 32)
		return (1);

	// Clear the Erase State Flag DMU_HF_OPERATION.ERASE
	//DMU_HF_CONTROL.B.CERASE = 1;

    // Clear Status
	MEM (0xAF005554) = 0xFA;
	pmuid = PMU_ID.U;

	// Erase Logical Sector Range
	MEM (0xAF00AA50) = (uint32) addr;
	pmuid = PMU_ID.U;
	MEM (0xAF00AA58) = (uint32) cnt;
	pmuid = PMU_ID.U;
	MEM (0xAF00AAA8) = (uint32) 0x80;
	pmuid = PMU_ID.U;
	MEM (0xAF00AAA8) = (uint32) 0x50;
	pmuid = PMU_ID.U;

	while(!DMU_HF_OPERATION.B.ERASE);
	while(DMU_HF_STATUS.B.D0BUSY); // ToDo

	return (0);
}

